package com.clubfactory.dtree;

public class DTreeAssertionException extends RuntimeException {

    public DTreeAssertionException(String s) {
        super(s);
    }

    public DTreeAssertionException() {
        super();
    }

    public DTreeAssertionException(String s, Throwable cause) {
        super(s, cause);
    }

    public DTreeAssertionException(Throwable cause) {
        super(cause);
    }

}
